package week5_programme;

import java.util.ArrayList;

public class Create5ArrayList {
    public static void main(String[] args) {
        ArrayList<String> c1 = new ArrayList<>();
        c1.add("Apple");
        c1.add("Banana");

        ArrayList<Integer> c2 = new ArrayList<>();
        c2.add(1);
        c2.add(2);

        ArrayList<Character> c3 = new ArrayList<>();
        c3.add('A');
        c3.add('B');

        ArrayList<Boolean> c4 = new ArrayList<>();
        c4.add(true);
        c4.add(false);

        ArrayList<Double> c5 = new ArrayList<>();
        c5.add(0.4);
        c5.add(5.7);
    }
}
